class Solution
{
    public:
    //Function to count number of ways to reach the nth stair 
    //when order does not matter.
    long long countWays(int m)
    {
        // total ways = number of times 2 can be used + 1
        // +1 for 0 2 is used

       return (long long)m/2+1;
    }
};

